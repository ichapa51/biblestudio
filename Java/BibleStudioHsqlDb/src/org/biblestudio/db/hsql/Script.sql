-- CREATE TABLES
CREATE TABLE Author (Id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY)
CREATE TABLE AuthorAlias (Id INTEGER NOT NULL, Name VARCHAR(128) NOT NULL, IsAbbr BOOLEAN NOT NULL, Priority INTEGER NOT NULL, Lang VARCHAR(2) NULL, PRIMARY KEY (Id, Name))
CREATE TABLE Bible (Id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY, Name VARCHAR(45) NOT NULL UNIQUE, Lang VARCHAR(2) NULL, IsTemplate BOOLEAN NOT NULL, IdTemplate INTEGER NULL, IdAuthor INTEGER NULL, IdCover INTEGER NULL, Version VARCHAR(20) NULL, Description VARCHAR(128) NULL, Comments LONGVARCHAR NULL)
CREATE TABLE BibleAlias (Id INTEGER NOT NULL, Name VARCHAR(128) NOT NULL, IsAbbr BOOLEAN NOT NULL, Priority INTEGER NOT NULL, Lang VARCHAR(2) NULL, PRIMARY KEY (Id, Name))
CREATE TABLE BookGroup (Id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY, IdBible INTEGER NOT NULL)
CREATE TABLE BookGroupAlias (Id INTEGER NOT NULL, Name VARCHAR(128) NOT NULL, IsAbbr BOOLEAN NOT NULL, Priority INTEGER NOT NULL, Lang VARCHAR(2) NULL, PRIMARY KEY (Id, Name))
CREATE TABLE Book (Id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY, IdBible INTEGER NOT NULL, IdGroup INTEGER NULL, IdAuthor INTEGER NULL, Year INTEGER NULL, Chapters INTEGER NULL, IdCover INTEGER NULL)
CREATE TABLE BookAlias (Id INTEGER NOT NULL, Name VARCHAR(128) NOT NULL, IsAbbr BOOLEAN NOT NULL, Priority INTEGER NOT NULL, Lang VARCHAR(2) NULL, PRIMARY KEY (Id, Name))
CREATE TABLE BookDiv (Id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY, IdBook INTEGER NOT NULL, DivKey VARCHAR(20), Title VARCHAR(128), UNIQUE (IdBook, DivKey))
CREATE TABLE BinaryFile (Id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY, Bytes BLOB)

